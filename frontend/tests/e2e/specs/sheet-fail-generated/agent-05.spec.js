test.describe(`Sheet fail verification | ${AGENT_NAME}`, () => {
  test.beforeEach(() => {
    test.skip(!RUN_SHEET_FAIL_GENERATED, 'Set RUN_SHEET_FAIL_GENERATED=true to execute fail verification tests')
  })

  for (const testCase of CASES) {
    test(`${testCase.id} | verify expected status | ${testCase.name}`, async ({ request }) => {
      const moduleName = String(testCase.module || '').toLowerCase()
      const pathName = String(testCase.path || '').toLowerCase()
      const caseExecutor = CASE_EXECUTORS[testCase.id]

      test.skip(SKIP_PAYMENT && (moduleName.includes('payment') || pathName.includes('/payment')), 'Payment cases disabled by SHEET_SKIP_PAYMENT=true')
      test.skip(!caseExecutor, 'No concrete endpoint mapping in this row; kept as manual verification')
      test.skip(!testCase.expectedStatus, 'Sheet row has no expected HTTP status')

      const result = await caseExecutor(request, testCase)
      const sheetFailureStatus = extractSheetFailureStatus(testCase)

      expect(result.durationMs, result.debug).toBeLessThanOrEqual(20_000)
      expect(result.status, result.debug).toBeGreaterThanOrEqual(100)
      expect(result.status, result.debug).toBeLessThan(600)
      expect(result.status, result.debug).not.toBe(401)

      if (sheetFailureStatus) {
        expect(
          [testCase.expectedStatus, sheetFailureStatus],
          `${result.debug} | expected=${testCase.expectedStatus} | sheetFailure=${sheetFailureStatus}`
        ).toContain(result.status)

        const reproduced = result.status === sheetFailureStatus
        test.info().annotations.push({
          type: 'sheet-repro',
          description: reproduced
            ? `reproduced: observed HTTP ${result.status}`
            : `not-reproduced: observed expected HTTP ${result.status}`,
        })
        return
      }

      expect(result.status, result.debug).toBe(testCase.expectedStatus)
    })
  }
})
