const TRANSIENT_STATUSES = new Set([502, 503, 504])
const authCache = new Map()

const sleep = async (ms) => new Promise((resolve) => setTimeout(resolve, ms))

const extractSheetFailureStatus = (testCase) => {
  const statuses = []

  for (const cell of testCase.rawCells || []) {
    const value = String(cell || '')
    const regex = /HTTP\s+(\d{3})/gi
    let match = regex.exec(value)

    while (match) {
      statuses.push(Number(match[1]))
      match = regex.exec(value)
    }
  }

  return statuses.find((status) => status !== testCase.expectedStatus) || null
}

const ensureLoggedIn = async (request, role) => {
  if (authCache.has(role)) {
    return authCache.get(role)
  }

  const account = ACCOUNTS[role]
  if (!account) {
    throw new Error(`Unsupported role for auth in ${AGENT_NAME}: ${role}`)
  }

  for (let attempt = 1; attempt <= 3; attempt += 1) {
    const response = await request.post(`${API_BASE_URL}/api/auth/login`, {
      headers: { 'Content-Type': 'application/json' },
      data: {
        email: account.email,
        password: account.password,
      },
    })

    if (response.ok()) {
      const payload = await response.json().catch(() => null)
      if (!payload?.token) {
        throw new Error(`Login for ${role} succeeded but token was missing`)
      }
      authCache.set(role, payload)
      return payload
    }

    const bodyText = await response.text().catch(() => '')
    const isTransient = TRANSIENT_STATUSES.has(response.status())
    if (!isTransient || attempt === 3) {
      throw new Error(`Login failed for ${role}: ${response.status()} ${bodyText.slice(0, 240)}`)
    }

    await sleep(300 * attempt)
  }

  throw new Error(`Login failed for ${role}: exhausted retries`)
}

const executeRequest = async (request, { testCase, method, path, token, data }) => {
  const url = new URL(path, API_BASE_URL)
  const headers = {
    'Content-Type': 'application/json',
    ...(token ? { Authorization: `Bearer ${token}` } : {}),
  }

  const start = Date.now()
  const response = await request.fetch(url.toString(), {
    method,
    headers,
    data,
  })
  const durationMs = Date.now() - start
  const responseText = await response.text().catch(() => '')

  return {
    status: response.status(),
    durationMs,
    debug: [
      `[${testCase.id}] ${method} ${url.pathname}${url.search}`,
      `status=${response.status()}`,
      `durationMs=${durationMs}`,
      `body=${responseText.slice(0, 240)}`,
    ].join(' | '),
  }
}

